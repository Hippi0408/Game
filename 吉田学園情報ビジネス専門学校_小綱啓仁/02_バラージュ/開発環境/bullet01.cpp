//*****************************************************************************
// 弾のセット処理
//*****************************************************************************
void CBullet01::BulletMove()
{
	float fMove = GetBulletMove();
	D3DXVECTOR3 move = GetMove();
	if (fMove < 5.0f)
	{
		fMove *= 1.05f;
	}
	D3DXVec3Normalize(&move, &move);
	move *= fMove;
	SetBulletMove(fMove);
	SetMove(move);

}
